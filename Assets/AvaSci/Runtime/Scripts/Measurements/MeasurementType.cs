namespace LightBuzz.AvaSci.Measurements
{
    /// <summary>
    /// The measurement type.
    /// </summary>
    ///
    [System.Serializable]
    public enum MeasurementType
    {
        /// <summary>
        /// Left elbow flexion.
        /// </summary>
        ElbowLeftFlexion,

        /// <summary>
        /// Right elbow flexion.
        /// </summary>
        ElbowRightFlexion,

        /// <summary>
        /// Left hip abduction.
        /// </summary>
        HipLeftAbduction,

        /// <summary>
        /// Left hip flexion.
        /// </summary>
        HipLeftFlexion,

        /// <summary>
        /// Right hip abduction.
        /// </summary>
        HipRightAbduction,

        /// <summary>
        /// Right hip flexion.
        /// </summary>
        HipRightFlexion,

        /// <summary>
        /// Left knee flexion.
        /// </summary>
        KneeLeftFlexion,

        /// <summary>
        /// Right knee flexion.
        /// </summary>
        KneeRightFlexion,

        /// <summary>
        /// Neck lateral flexion (side bend).
        /// </summary>
        NeckLateralFlexion,

        /// <summary>
        /// Neck rotation.
        /// </summary>
        NeckRotation,

        /// <summary>
        /// Left shoulder abduction.
        /// </summary>
        ShoulderLeftAbduction,

        /// <summary>
        /// Left shoulder rotation.
        /// </summary>
        ShoulderLeftRotation,

        /// <summary>
        /// Right shoulder abduction.
        /// </summary>
        ShoulderRightAbduction,

        /// <summary>
        /// Right shoulder rotation.
        /// </summary>
        ShoulderRightRotation,
        ShoulderLeftFlexion,
        ShoulderRightFlexion,

        /// <summary>
        /// None.
        /// </summary>
        None
    }
}